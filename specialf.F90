   function K1_func(x) result(res)
      implicit none
      real(dp), intent(in) :: x
      real(dp) :: res, d, dd, y, y2, sv, dpi
      integer :: i
      dpi = 3.1415926535897932385d0
      d = 0
      dd = 0d0
      y = (2d0 * x + mxxm) * oxxm
      y2 = 2d0 * y
      if (x .gt. xmax) then
         res = 5d-1 * (dlog(5d-1 * dpi) - x) - dexp(x)
      else if (x .lt. xmin) then
         res = - x
      else
         do i = 120, 2, -1
            sv = d
            d = y2 * d - dd + Ftab(i)
            dd = sv
         enddo
         res = y * d - dd + 5d-1 * Ftab(1)
      end if
   end function K1_func

